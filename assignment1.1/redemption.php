        <h2>
            Redemption
        </h2>
        <p>
            Bottle redemption is a great way to help bring down the rate of 
            filling landfills and trash lying around everywhere. Bottle 
            redemption encourages people to bring their bottles to a recycling 
            facility in exchange for money returned. Even though most bottles 
            are only 5 cents each normally, once you get enough it can amount to
            a lot of money that will get returned. Since the bottles will get 
            returned to a collection facility they will be melted down and 
            repurposed by the plant that will affiliate with the facility. So in
            essence this is a great method to help the environment and make a 
            little of your hard earned money back.
